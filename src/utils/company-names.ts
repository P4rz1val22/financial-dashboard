export class CompanyNameResolver {
  static getCompanyName(symbol: string): string | null {
    const symbolUpper = symbol.toUpperCase();
    return COMPANY_NAME_MAP[symbolUpper] || null;
  }

  static hasMapping(symbol: string): boolean {
    const symbolUpper = symbol.toUpperCase();
    return symbolUpper in COMPANY_NAME_MAP;
  }

  static getMappedSymbols(): string[] {
    return Object.keys(COMPANY_NAME_MAP);
  }

  static getMappingCount(): number {
    return Object.keys(COMPANY_NAME_MAP).length;
  }
}
